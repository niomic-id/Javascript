function TestSwitch() {
    var nilai = 5;

    switch (nilai) {
        case 10:
            console.log("Sangat Bagus");
            break;
        case 8:
            console.log("Baik");
            break;
        case 6:
            console.log("Cukup");
            break;
        default:
            console.log("Lainya");
            break;
    }   
}

TestSwitch();